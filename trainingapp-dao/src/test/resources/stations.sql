DROP TABLE STATION_LINE IF EXISTS;
DROP TABLE STATION IF EXISTS;
DROP TABLE LINE IF EXISTS;

CREATE TABLE STATION(ID INT GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1) PRIMARY KEY,
    NAME VARCHAR(100) NOT NULL,
);

CREATE TABLE LINE(ID INT GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1) PRIMARY KEY,
    NAME VARCHAR(100) NOT NULL,
);

CREATE TABLE STATION_LINE(
    ID INT GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1) PRIMARY KEY,
    STATION_ID INT FOREIGN KEY REFERENCES STATION(ID),
    LINE_ID INT FOREIGN KEY REFERENCES LINE(ID),
    ORDER_STATION INT
);



